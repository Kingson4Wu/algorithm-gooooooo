/*
*
用一个stack保存为处理完的下标
用结果数组ans直接计数
*/
func dailyTemperatures(temperatures []int) []int {

	n := len(temperatures)
	ans := make([]int, n)
	var stack []int
	stack = append(stack, 0)
	for i := 1; i < n; i++ {
		for k := len(stack) - 1; k >= 0; k-- {
			ans[stack[k]]++
			if temperatures[i] > temperatures[stack[k]] {
				stack = stack[0 : len(stack)-1]
			}
		}
		stack = append(stack, i)
	}
	//最终没找到更大的，全部置0
	for k := len(stack) - 1; k >= 0; k-- {
		ans[stack[k]] = 0
	}
	return ans
}

